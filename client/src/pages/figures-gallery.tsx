function FigureCard({ fig, onClick }: { fig: Figure; onClick: () => void }) {
  return (
    <div
      className="bg-gray-900 border border-gray-700 rounded-xl overflow-hidden hover:border-gray-500 transition-colors cursor-pointer group"
      onClick={onClick}
      data-testid={`figure-card-${fig.id}`}
    >
      <div className="bg-gray-800 h-44 flex items-center justify-center overflow-hidden relative">
        <img
          src={fig.file}
          alt={fig.title}
          className="max-h-full max-w-full object-contain p-2"
          loading="lazy"
          onError={(e) => {
            const img = e.target as HTMLImageElement;
            img.style.display = "none";
            const parent = img.parentElement;
            if (parent && !parent.querySelector(".fallback-label")) {
              const div = document.createElement("div");
              div.className = "fallback-label text-gray-500 text-sm text-center p-4";
              div.innerHTML = `${fig.type.toUpperCase()} Figure<br/><span style="font-size:11px">${fig.figNum}</span>`;
              parent.appendChild(div);
            }
          }}
        />
        <div className="absolute top-2 right-2 opacity-0 group-hover:opacity-100 transition-opacity">
          <a
            href={fig.file}
            download
            onClick={(e) => e.stopPropagation()}
            className="bg-gray-900/90 text-white text-xs px-2 py-1 rounded border border-gray-600 hover:bg-gray-700"
            data-testid={`download-${fig.id}`}
          >
            ↓ Download
          </a>
        </div>
      </div>
      <div className="p-4">
        <div className="flex items-center gap-2 mb-1 flex-wrap">
          <span className="text-xs font-semibold text-emerald-400">{fig.paperLabel}</span>
          <span className="text-xs text-gray-500">·</span>
          <span className="text-xs text-gray-400">{fig.figNum}</span>
          <span className={`ml-auto text-xs px-2 py-0.5 rounded border font-medium ${fig.statusColor}`}>
            {fig.type.toUpperCase()}
          </span>
        </div>
        <h3 className="text-sm font-semibold text-white mb-1 leading-snug">{fig.title}</h3>
        <p className="text-xs text-gray-400 leading-relaxed line-clamp-3">{fig.description}</p>
        <div className="mt-2 flex flex-wrap items-center gap-2">
          <span className={`text-xs px-2 py-0.5 rounded border ${fig.statusColor}`}>{fig.status}</span>
          {fig.interactive && (
            <a
              href={fig.interactive}
              onClick={(e) => e.stopPropagation()}
              className="text-xs text-purple-400 hover:text-purple-300 underline"
            >
              Interactive →
            </a>
          )}
        </div>
      </div>
    </div>
  );
}

