export default function FiguresGallery() {
  const [filter, setFilter] = useState("all");
  const [search, setSearch] = useState("");
  const [lightbox, setLightbox] = useState<Figure | null>(null);

  const visible = FIGURES.filter((f) => {
    const matchPaper = filter === "all" || f.paper === filter;
    const q = search.toLowerCase();
    const matchSearch =
      q === "" ||
      f.title.toLowerCase().includes(q) ||
      f.description.toLowerCase().includes(q) ||
      f.paperLabel.toLowerCase().includes(q) ||
      f.figNum.toLowerCase().includes(q);
    return matchPaper && matchSearch;
  });

  return (
    <div className="min-h-screen bg-gray-950 text-gray-100">
      {lightbox && <LightboxModal fig={lightbox} onClose={() => setLightbox(null)} />}
      <div className="max-w-7xl mx-auto px-4 py-8">

        <div className="mb-8">
          <div className="flex items-center gap-3 mb-2">
            <div className="w-2 h-8 bg-emerald-500 rounded-full" />
            <h1 className="text-3xl font-bold text-white">Figures Gallery</h1>
          </div>
          <p className="text-gray-400 ml-5 text-lg">
            All {FIGURES.length} figures, diagrams, and visualizations across all PAR(2) papers — view and download
          </p>
        </div>

        <div className="flex flex-col sm:flex-row gap-3 mb-5">
          <input
            type="text"
            placeholder="Search by title, description, gene name…"
            value={search}
            onChange={(e) => setSearch(e.target.value)}
            className="bg-gray-800 border border-gray-700 text-white placeholder-gray-500 rounded-lg px-4 py-2 text-sm flex-1 focus:outline-none focus:border-emerald-500"
            data-testid="input-search-figures"
          />
          <a
            href="/figures/all-figures.zip"
            download
            className="bg-emerald-600 hover:bg-emerald-700 text-white text-sm px-4 py-2 rounded-lg font-medium whitespace-nowrap flex items-center gap-1"
            data-testid="link-download-all"
          >
            ↓ Download All ({FIGURES.length} files)
          </a>
        </div>

        <div className="flex flex-wrap gap-2 mb-5">
          {PAPERS.map((p) => {
            const count = p.id === "all" ? FIGURES.length : FIGURES.filter((f) => f.paper === p.id).length;
            return (
              <button
                key={p.id}
                onClick={() => setFilter(p.id)}
                data-testid={`filter-${p.id}`}
                className={`text-xs px-3 py-1.5 rounded-full border font-medium transition-colors ${
                  filter === p.id
                    ? "bg-emerald-600 border-emerald-500 text-white"
                    : "bg-gray-800 border-gray-700 text-gray-400 hover:border-gray-500 hover:text-gray-200"
                }`}
              >
                {p.label} <span className="opacity-60">({count})</span>
              </button>
            );
          })}
        </div>

        <p className="text-sm text-gray-500 mb-4">
          Showing {visible.length} of {FIGURES.length} figures
        </p>

        {visible.length === 0 ? (
          <div className="text-center py-16 text-gray-500">No figures match your search.</div>
        ) : (
          <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 xl:grid-cols-4 gap-5">
            {visible.map((fig) => (
              <FigureCard key={fig.id} fig={fig} onClick={() => setLightbox(fig)} />
            ))}
          </div>
        )}

        <div className="mt-10 pt-6 border-t border-gray-800 text-xs text-gray-500 flex flex-wrap gap-4">
          <span>Click any card to preview full-size with description.</span>
          <span>Hover a card to reveal the quick download button.</span>
          <span>Interactive links open the live analysis page for that figure.</span>
        </div>
      </div>
    </div>
  );
}
